namespace MyStash.Export
{
    /// <summary>
    /// Character encoding type. Output can encoded as ANSI, Ascii, UTF7, UTF8 or Unicode (default)
    /// </summary>
    public enum Encoding
    {
        /// <summary>
        /// ANSI encoding.
        /// </summary>
        Ansi,
        /// <summary>
        /// Ascii encoding
        /// </summary>
        Ascii,
        /// <summary>
        /// UTF7 encoding
        /// </summary>
        UTF7,
        /// <summary>
        /// UFT8 encoding
        /// </summary>
        UTF8,
        /// <summary>
        /// Unicode encoding
        /// </summary>
        Unicode
    }
}
